    private void clasesTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clasesTableMouseClicked
        int fila = clasesTable.getSelectedRow();
        int id = (int) clasesTable.getValueAt(fila, 0);
        String nombre = clasesTable.getValueAt(fila, 1).toString();
        String link = clasesTable.getValueAt(fila, 2).toString();
        String maestro = clasesTable.getValueAt(fila, 3).toString();
        Clase nc = new Clase(nombre, link, maestro, id);
        
        ClaseInfo ci = new ClaseInfo(nc);
        ci.setVisible(true);
        dispose();
    }//GEN-LAST:event_clasesTableMouseClicked

